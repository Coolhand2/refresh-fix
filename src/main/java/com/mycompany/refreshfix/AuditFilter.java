import java.io.IOException;
import javax.servlet.Filter;
import javax.servlet.FilterChain;
import javax.servlet.FilterConfig;
import javax.servlet.ServletException;
import javax.servlet.ServletRequest;
import javax.servlet.ServletResponse;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

/**
 *
 * @author Sherwood
 */
public class AuditFilter implements Filter {

    private static final Logger LOG = LoggerFactory.getLogger(AuditFilter.class);

    public AuditFilter() {
    }

    /**
     *
     * @param request The servlet request we are processing
     * @param response The servlet response we are creating
     * @param chain The filter chain we are processing
     *
     * @exception IOException if an input/output error occurs
     * @exception ServletException if a servlet error occurs
     */
    @Override
    public void doFilter(ServletRequest r1, ServletResponse r2, FilterChain fc) throws IOException, ServletException {
        HttpServletRequest request = (HttpServletRequest) r1;
        HttpServletResponse response = (HttpServletResponse) r2;
        fc.doFilter(r1, r2);
    }

    /**
     * Destroy method for this filter
     */
    @Override
    public void destroy() {
    }

    /**
     * Init method for this filter
     */
    @Override
    public void init(final FilterConfig filterConfig) {
    }
}
